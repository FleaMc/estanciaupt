    private void bt_registrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_registrarMouseClicked
        if (nombreEstaVacio()) return;
        
        software = tf_nombreDeSoftware.getText();
        
        Administrador.registrarSoftware(software);
        
        bd.abrirConexion();
        llenarTablaDeSoftware(bd.consultarSoftware());
        bd.cerrarConexion();
    }//GEN-LAST:event_bt_registrarMouseClicked

    private void bt_eliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_eliminarMouseClicked
        if (nombreEstaVacio()) return;
        
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Desea eliminar el software " + software + "?", "Confirmar Baja", JOptionPane.OK_CANCEL_OPTION);

        if (confirmacion == 0) {
            software = tf_nombreDeSoftware.getText();
            
            Administrador.eliminarSoftware(software);

            bd.abrirConexion();
            llenarTablaDeSoftware(bd.consultarSoftware());
            bd.cerrarConexion();
        }
    }//GEN-LAST:event_bt_eliminarMouseClicked

    private void tb_softwareMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_softwareMouseClicked
        numFilaSeleccionada = tb_software.rowAtPoint(evt.getPoint());
        
        software = tb_software.getValueAt(numFilaSeleccionada, 0)+"";

        tf_nombreDeSoftware.setText(software);
    }//GEN-LAST:event_tb_softwareMouseClicked

