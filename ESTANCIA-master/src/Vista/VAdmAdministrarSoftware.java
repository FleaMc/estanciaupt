    private void llenarTablaDeSoftware(ResultSet rs) {
        try {
            DefaultTableModel dtm = (DefaultTableModel) tb_software.getModel();
            dtm.setRowCount(0);
            
            if (rs != null) {
                while (rs.next()) {
                    String[] fila = {rs.getString("Software")};
                    dtm.addRow(fila);
                }
            }
            tb_software.setModel(dtm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: No se pudó realizar la consulta");
        }
    }
    
    private boolean nombreEstaVacio() {
        if (tf_nombreDeSoftware.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El nombre del software esta vacío");
            return true;
        } else {
            return false;
        }
    }
