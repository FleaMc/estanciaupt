    private void tb_computadorasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_computadorasMouseClicked
        numFilaSeleccionada = tb_computadoras.rowAtPoint(evt.getPoint());
        
        String pkId = tb_computadoras.getValueAt(numFilaSeleccionada, 0)+"";
        EstatusPc estatus = EstatusPc.valueOf(tb_computadoras.getValueAt(numFilaSeleccionada, 1)+"");
        Componente mouse = new Componente(tb_computadoras.getValueAt(numFilaSeleccionada, 2)+"", tb_computadoras.getValueAt(numFilaSeleccionada, 3)+"");
        Componente teclado = new Componente(tb_computadoras.getValueAt(numFilaSeleccionada, 4)+"", tb_computadoras.getValueAt(numFilaSeleccionada, 5)+"");
        Componente monitor = new Componente(tb_computadoras.getValueAt(numFilaSeleccionada, 6)+"", tb_computadoras.getValueAt(numFilaSeleccionada, 7)+"");
        Componente procesador = new Componente(tb_computadoras.getValueAt(numFilaSeleccionada, 8)+"", tb_computadoras.getValueAt(numFilaSeleccionada, 9)+"");
        Componente ram = new Componente(tb_computadoras.getValueAt(numFilaSeleccionada, 10)+"", tb_computadoras.getValueAt(numFilaSeleccionada, 11)+"");
        Componente almacenamiento = new Componente(tb_computadoras.getValueAt(numFilaSeleccionada, 12)+"", tb_computadoras.getValueAt(numFilaSeleccionada, 13)+"");

        computadora = new Computadora(pkId, salon, estatus, mouse, teclado, monitor, procesador, ram, almacenamiento);

        lb_idComputador.setText(pkId);
        tf_mouseDescripcion.setText(mouse.getDescripcion());
        tf_mouseNumSerie.setText(mouse.getNumSerie());
        tf_tecladoDescripcion.setText(teclado.getDescripcion());
        tf_tecladoNumSerie.setText(teclado.getNumSerie());
        tf_monitorDescripcion.setText(monitor.getDescripcion());
        tf_monitorNumSerie.setText(monitor.getNumSerie());
        tf_procesadorDescripcion.setText(procesador.getDescripcion());
        tf_procesadorNumSerie.setText(procesador.getNumSerie());
        tf_ramDescripcion.setText(ram.getDescripcion());
        tf_ramNumSerie.setText(ram.getNumSerie());
        tf_almacenamientoDescripcion.setText(almacenamiento.getDescripcion());
        tf_almacenamientoNumSerie.setText(almacenamiento.getNumSerie());
    }//GEN-LAST:event_tb_computadorasMouseClicked

    private void bt_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_modificarActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Desea modificar las características de la PC: " + computadora.getPkId() + "?", "Confirmar cambios", JOptionPane.OK_CANCEL_OPTION);

        if (confirmacion == 0) {
            Componente mouse = new Componente(tf_mouseDescripcion.getText(), tf_mouseNumSerie.getText());
            Componente teclado = new Componente(tf_tecladoDescripcion.getText(), tf_tecladoNumSerie.getText());
            Componente monitor = new Componente(tf_monitorDescripcion.getText(), tf_monitorNumSerie.getText());
            Componente procesador = new Componente(tf_procesadorDescripcion.getText(), tf_procesadorNumSerie.getText());
            Componente ram = new Componente(tf_ramDescripcion.getText(), tf_ramNumSerie.getText());
            Componente almacenamiento = new Componente(tf_almacenamientoDescripcion.getText(), tf_almacenamientoNumSerie.getText());

            computadora.setMouse(mouse);
            computadora.setTeclado(teclado);
            computadora.setMonitor(monitor);
            computadora.setProcesador(procesador);
            computadora.setRam(ram);
            computadora.setAlmacenamiento(almacenamiento);

            Administrador.modificarComputadora(computadora);

            bd.abrirConexion();
            llenarTablaDeComputadoras(bd.consultarInventario(salon));
            bd.cerrarConexion();
            //limpiarCampos();
        }
    }//GEN-LAST:event_bt_modificarActionPerformed

