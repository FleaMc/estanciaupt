    private void llenarTablaDeComputadoras(ResultSet rs) {
        try {
            DefaultTableModel dtm = (DefaultTableModel) tb_computadoras.getModel();
            dtm.setRowCount(0);
            
            if (rs != null) {
                while (rs.next()) {
                    String[] fila = {rs.getString("pkId"), rs.getString("fkEstatusPc"), rs.getString("MouseDescripcion"), rs.getString("MouseNumSerie"), rs.getString("TecladoDescripcion"), rs.getString("TecladoNumSerie"), rs.getString("MonitorDescripcion"), rs.getString("MonitorNumSerie"), rs.getString("ProcesadorDescripcion"), rs.getString("ProcesadorNumSerie"), rs.getString("RamDescripcion"), rs.getString("RamNumSerie"), rs.getString("AlmacenamientoDescripcion"), rs.getString("AlmacenamientoNumSerie")};
                    dtm.addRow(fila);
                }
            }
            tb_computadoras.setModel(dtm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: No se pudó realizar la consulta");
        }
    }
    
