    private void llenarTablaDeAlumnos(ResultSet rs) {
        try {
            DefaultTableModel dtm = (DefaultTableModel) tb_alumnos.getModel();
            dtm.setRowCount(0);

            if (rs != null) {
                while (rs.next()) {
                    String[] fila = {rs.getString("pkId"), rs.getString("Nombre"), rs.getString("Contrasena"), rs.getString("fkEstatus"), rs.getString("fkEstado")};
                    dtm.addRow(fila);
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
