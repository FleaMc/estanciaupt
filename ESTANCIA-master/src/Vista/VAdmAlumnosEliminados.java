    private void bt_restaurarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_restaurarAlumnoActionPerformed
        if (alumno == null ) {
            JOptionPane.showMessageDialog(null, "Da click en un alumno primero");
            return;
        }
        
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Desea restaurar al alumno " + alumno.getPkId() + "?", "Confirmar Baja", JOptionPane.OK_CANCEL_OPTION);

        if (confirmacion == 0) {
            alumno.setFkEstado(Estado.Activo);
            Administrador.establecerEstadoDeUsuario(alumno);
            
            bd.abrirConexion();
            llenarTablaDeAlumnos(bd.consultarAlumnosEliminados());
            bd.cerrarConexion();
        }
    }//GEN-LAST:event_bt_restaurarAlumnoActionPerformed

