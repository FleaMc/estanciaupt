    private void llenarTablaDeDocentes(ResultSet rs) {
        try {
            DefaultTableModel dtm = (DefaultTableModel) tb_docentes.getModel();
            dtm.setRowCount(0);

            if (rs != null) {
                while (rs.next()) {
                    String[] fila = {rs.getString("pkId"), rs.getString("Nombre"), rs.getString("Contrasena")};
                    dtm.addRow(fila);
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    private void limpiarCampos() {
        tf_nombreDelDocente.setText("");
        tf_idDelDocente.setText("");
        tf_contrasena.setText("");
    }
    
    private boolean camposEstanVacios() {
        if (tf_nombreDelDocente.getText().isEmpty() || tf_idDelDocente.getText().isEmpty() || tf_contrasena.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos los campos deben llenarse");
            return true;
        } else {
            return false;
        }
    }
