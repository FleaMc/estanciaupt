    private void tb_docentesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_docentesMouseClicked
        numFilaSeleccionada = tb_docentes.rowAtPoint(evt.getPoint());

        docente = new Docente(Integer.parseInt(tb_docentes.getValueAt(numFilaSeleccionada, 0)+""),tb_docentes.getValueAt(numFilaSeleccionada, 1)+"",tb_docentes.getValueAt(numFilaSeleccionada, 2)+"");
        
        tf_idDelDocente.setText(docente.getPkId()+"");
        tf_nombreDelDocente.setText(docente.getNombre());
        tf_contrasena.setText(docente.getContrasena());
    }//GEN-LAST:event_tb_docentesMouseClicked

    private void bt_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_registrarActionPerformed
        if (camposEstanVacios()) return;

        Usuario docenteNuevo = new Docente(Integer.parseInt(tf_idDelDocente.getText()), tf_nombreDelDocente.getText(), tf_contrasena.getText());
        
        Administrador.registrarUsuario(docenteNuevo);
        
        bd.abrirConexion();
        llenarTablaDeDocentes(bd.consultarDocentes());
        bd.cerrarConexion();
        limpiarCampos();
    }//GEN-LAST:event_bt_registrarActionPerformed

    private void bt_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_modificarActionPerformed
        if (camposEstanVacios()) return;
        
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Desea confirmar los siguientes cambios para el docente " + docente.getPkId() + "?:\nNombre:\n   " + docente.getNombre() + " -> " + tf_nombreDelDocente.getText() + "\nContraseña:\n   " + docente.getContrasena() + " -> " + tf_contrasena.getText(), "Confirmar cambios", JOptionPane.OK_CANCEL_OPTION);

        if (confirmacion == 0) {
            // El id no se puede modificar, por ello se obtiene del docente creado a partir del mouse clicked
            docente.setNombre(tf_nombreDelDocente.getText());
            docente.setContrasena(tf_contrasena.getText());

            Administrador.modificarUsuario(docente);

            bd.abrirConexion();
            llenarTablaDeDocentes(bd.consultarDocentes());
            bd.cerrarConexion();
            limpiarCampos();
        }
    }//GEN-LAST:event_bt_modificarActionPerformed

    private void bt_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_eliminarActionPerformed
        if (tf_idDelDocente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un id para poder dar de baja");
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Desea eliminar al docente " + docente.getPkId() + "?", "Confirmar Baja", JOptionPane.OK_CANCEL_OPTION);

        if (confirmacion == 0) {
            docente.setFkEstado(Estado.Eliminado);
            Administrador.establecerEstadoDeUsuario(docente);
            
            bd.abrirConexion();
            llenarTablaDeDocentes(bd.consultarDocentes());
            bd.cerrarConexion();
            limpiarCampos();
        }
    }//GEN-LAST:event_bt_eliminarActionPerformed

